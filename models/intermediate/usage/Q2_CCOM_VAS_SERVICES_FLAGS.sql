SELECT  
    A.CALL_DT,
    A.A_NR,
    A.A_NR_To_Join,
    A.CTRY_To_Join,
    A.IN_CDR_FILE_ID,        
    A.IN_CDR_SERIAL_NR,
    A.OUT_CDR_FILE_ID,       
    A.OUT_CDR_SERIAL_NR,
    RPT_PORTIN.LINK_ID,
    MAX(
        CASE 
            WHEN SRV.ACCESS_NUMBER IS NOT NULL 
                 AND SRV.SERVICE_DESCRIPTION = 'National Outbound Call' 
            THEN SRV.IS_ACTIVE 
            ELSE NULL 
        END
    ) AS VAS_NAT_OUT_CALL_SRV_FLAG,
    RPT_PORTIN.PORT_IN AS VAS_PORT_IN,
    COALESCE(RPT_PORTOUT.PORT_OUT, 0) AS VAS_PORT_OUT,
    A.DIRECTION,
    A.IN_VAS_ACCESS_NUMBER,
    A.OUT_VAS_ACCESS_NUMBER
FROM (
    -- Precompute A_NR_To_Join and CTRY_To_Join
    SELECT  
        A.*,
        CASE 
            WHEN A.DIRECTION = 'OUTBOUND' THEN A.A_NR
            ELSE COALESCE(A.IN_VAS_ACCESS_NUMBER, A.OUT_VAS_ACCESS_NUMBER) 
        END AS A_NR_To_Join,
        CASE 
            WHEN A.DIRECTION = 'OUTBOUND' THEN A.A_NR_CTRY_CD
            ELSE A.B_NR_CTRY_CD 
        END AS CTRY_To_Join
    FROM {{ref('Q2_IBIS_CALL_MAPFULL_CCOM_RATING_updated_fields')}} A
) A
LEFT JOIN (
    SELECT  
        R2.ACCESS_NUMBER, 
        R2.LINK_ID,
        R2.ORIGIN_COUNTRY,
        R2.PORT_IN,
        R2.START_DATE,
        R2.END_DATE
    FROM {{ref('Q2_STG_VAS_PORTIN_FINAL_1')}} R2
) RPT_PORTIN
ON A.CALL_DT BETWEEN RPT_PORTIN.START_DATE AND RPT_PORTIN.END_DATE
AND A.A_NR_To_Join = RPT_PORTIN.ACCESS_NUMBER
AND A.CTRY_To_Join = RPT_PORTIN.ORIGIN_COUNTRY

LEFT JOIN {{ref('VAS_EXT_DM_RPT_SRV')}} SRV
ON SRV.ACCESS_NUMBER = RPT_PORTIN.ACCESS_NUMBER
AND RPT_PORTIN.LINK_ID IS NOT NULL
AND SRV.LINK_ID = RPT_PORTIN.LINK_ID
AND SRV.SERVICE_DESCRIPTION = 'National Outbound Call'
AND A.CALL_DT BETWEEN COALESCE(SRV.VALID_FROM, DATE('1900-01-01')) 
                 AND COALESCE(SRV.VALID_TO, DATE('2099-12-31'))

LEFT JOIN (
    SELECT  
        R3.ACCESS_NUMBER, 
        R3.LINK_ID,
        R3.ORIGIN_COUNTRY,
        R3.PORT_OUT,
        R3.START_DATE,
        R3.END_DATE
    FROM {{ref('Q2_STG_VAS_PORTOUT_FINAL_1')}} R3
) RPT_PORTOUT
ON A.CALL_DT BETWEEN RPT_PORTOUT.START_DATE AND RPT_PORTOUT.END_DATE
AND A.A_NR_To_Join = RPT_PORTOUT.ACCESS_NUMBER
AND A.CTRY_To_Join = RPT_PORTOUT.ORIGIN_COUNTRY

WHERE A.IN_VAS_ACCESS_NUMBER IS NOT NULL 
   OR A.OUT_VAS_ACCESS_NUMBER IS NOT NULL

GROUP BY 
    A.CALL_DT, A.A_NR, A.A_NR_To_Join, A.CTRY_To_Join, 
    A.IN_CDR_FILE_ID, A.IN_CDR_SERIAL_NR, 
    A.OUT_CDR_FILE_ID, A.OUT_CDR_SERIAL_NR, 
    RPT_PORTIN.LINK_ID, 
    RPT_PORTIN.PORT_IN, 
     RPT_PORTOUT.PORT_OUT,
    A.DIRECTION, 
    A.IN_VAS_ACCESS_NUMBER, A.OUT_VAS_ACCESS_NUMBER
